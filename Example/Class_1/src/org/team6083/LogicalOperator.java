package org.team6083;

public class LogicalOperator {

	public static void main(String[] args) {
		System.out.println(true && true);//輸出true
		System.out.println(true && false);//輸出false
		System.out.println(true || true);//輸出true
		System.out.println(true || false);//輸出true
		System.out.println(!true);//輸出false
		System.out.println((!false)&&(true||false));
	}

}
